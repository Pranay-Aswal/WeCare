     <div>
     <ul style="margin-left: 120px; margin-right: 120px; font-size: 17px; color: #151F05;">
     <li>&#8226; Learn to recognize when houseplants need water. Provide your plants with enough water to keep the soil moist but not soggy</li>
     <li>&#8226; Be aware of temperature, humidity, and ventilation. Most houseplants thrive in temperatures between 65 and 75°F during the day and about 10 degrees cooler at night. Placing a fan near your houseplants to circulate air can evaporate excess moisture and prevent dust buildup on leaves.</li>
     <iframe width="560" height="315" src="https://www.youtube.com/embed/LZhnCxG5c6s" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen style="margin-top: 20px; margin-bottom: 15px; margin-left: 27%;"></iframe>
     <li>&#8226; Ensure that your houseplants get the right amount of light. All plants need light energy for photosynthesis, but different houseplants require different amounts of light. With the exception of desert cacti and other succulents, most houseplants need indirect light rather than direct light.</li>
     <li>&#8226; Use the right potting soil. A high-quality potting soil will help plant roots grow by providing the ideal balance of nutrition, aeration, and water absorption.</li>
     <li>&#8226; Select a pot that fits your plant. When choosing a pot, make sure to consider its material, size, and drainage capability. Use a pot that’s proportional to your plant's current size—not more than a few inches wider in diameter than your plant’s root mass.</li>
