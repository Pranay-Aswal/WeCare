     <li>&#8226; Once the plant outgrows its home, you can transplant it into a larger pot. If you instead start a plant in a larger pot than necessary, its roots won't be able to absorb moisture fast enough as it drains through the soil.</li>
     <li>&#8226;  Use fertilizer to supply nutrients. In general, fertilize your houseplants once a month when they're growing or flowering. </li>
     <li>&#8226; During the winter months when plants typically stay in a stagnant state, it's acceptable to decrease or pause your fertilizer regimen.</li>
      </ul>
